package com;

public class DataType {
	public static void main(String[] args) {
		String Name ="Niraj Kumar";					// Non-Primitive DataType
		int age = 23;								// Primitive DataType
		String company = "Oracle";					// Non-Primitive DataType
		int salary = 320000;						// Primitive DataType
		float service= 4.5f;						// Primitive DataType
		char gender= 'M';							// Primitive DataType
		boolean postgraduated= false;				// Primitive DataType
		System.out.println("Employee Name = " +Name);
		System.out.println("Age of the Employee = " +age);
		System.out.println("Current company Name = " +company);
		System.out.println("Employee Salary = " +salary);
		System.out.println("Employee Experience in Years = " +service);
		System.out.println("Employee Gender = " +gender);
		System.out.println("Employee Postgraduated Status =" +postgraduated);

	}

}
