package com;

public class Percentage {
	public static void main(String[] args) {
		int p=78;
		int c=97;
		int m =82;
		int a=86;
		int total= (p+c+m+a);
		double percentage= total/4.0;
		System.out.println("Total Marks = " + total);
		System.out.println("Percentage = " + percentage + "%");

	}
}
