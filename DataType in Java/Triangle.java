package com;

public class Triangle {
	public static void main(String[] args) {
		float l=3.4f;
		float b = 5.2f;
		float area=0.5f*l*b;
		System.out.println("Length of triangle = " +l);
		System.out.println("Base of triangle = " +b);
		System.out.println("Area of triangle = " +area);
	}

}
